package com.javarush.task.task04.task0433;


/* 
Гадание на долларовый счет
*/

import java.io.*;

public class Solution {
    public static void main(String[] args) throws Exception {
       int i = 0;
       while(i < 10){
           i++;
           int j = 0;
           while(j < 10){
               j++;
               System.out.print("S");
           }
           System.out.println();
       }
    }
}
